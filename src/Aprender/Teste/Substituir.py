import re

def substituir_asterisco_pelo_ultimo_numero(frases):
    frases_modificadas = []

    for frase in frases:
        # Encontrar o último número na frase
        numeros = re.findall(r'\d+', frase)
        ultimo_numero = numeros[-1] if numeros else None

        # Substituir '*' pelo último número
        nova_frase = frase.replace('*' , str(ultimo_numero) if ultimo_numero else '')

        frases_modificadas.append(nova_frase.strip())

    return frases_modificadas

