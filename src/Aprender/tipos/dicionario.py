#Para adicionar um dicionario, abre e fecha "chaves", contendo chave + valor
aluno = {
    'nome': 'Pedro Henrique',
    'nota': 9.2,
    'ativo':True

}

print(type(aluno))                                            #Type 'dict'
print(aluno['nome'])                                          #Mostra informação conforme chave selecionada 'nome', 'nota', 'ativo'
print(aluno['nota'])
print(aluno['ativo'])
print(len(aluno))                                             #Mostra quantas chaves tenho neste dicionario
