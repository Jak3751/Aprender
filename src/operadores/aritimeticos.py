x = 10
y = 3

# Exemplos de sintaxe aritimeticos
# +3 >> prefix
# x++ >> posfix
# x * y >> infix

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)                                                             # % significa que é o modulo é o resto da divisão

# Exemplo de como descobrir se um numero é par ou impar utilizando o modulo que é o resto da divisão
par = 34
impar = 38

print(par % 2 == 0)                                                      # True
print(impar % 2 == 1)                                                    # False